# Importing math module / Library
from math import *

print(34)  # Printing an integer
print(3.4523)  # printing a decimal fraction
print(3 + 2.5)  # Basic arithmetic. Parenthesis declares order of operations

# Storing numbers as a variable
myNum = 6
print(myNum)

# Converting numbers into a string
print(str(myNum) + ' is my favourite number')

# Mathematical functions
myAbs = -2.3
print(abs(myAbs))  # Prints absolute value
print(pow(2, 4))  # X raised to power Y
print(max(3, 6))  # Largest among the two is returned
print(min(3, 5))  # Smallest among the two is returned
print(round(3.234))  # Normal rounding
print(floor(3.23425364))  # The largest integer smaller than the parameter
print(ceil(3.02342))  # The smallest integer larger than the parameter
print(sqrt(36))  # Square root
